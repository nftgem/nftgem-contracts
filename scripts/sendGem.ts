// We require the Hardhat Runtime Environment explicitly here. This is optional
// but useful for running the script in a standalone fashion through `node <script>`.
//
// When running the script with `hardhat run <script>` you'll find the Hardhat
// Runtime Environment's members available in the global scope.
import hre from 'hardhat';
const {ethers, deployments} = hre;
const {get} = deployments;
const {Contract} = ethers;
import {pack, keccak256} from '@ethersproject/solidity';

async function main() {
  // Hardhat always runs the compile task when running scripts with its command
  // line interface.
  //
  // If this script is run directly using `node` you may want to call compile
  // manually to make sure everything is compiled
  // await hre.run('compile');

  // We get the signer
  const [sender] = await hre.ethers.getSigners();

  // get the Gem pools contract
  const NFTGemPoolFactory = await hre.ethers.getContractAt(
    'NFTGemPoolFactory',
    (await hre.ethers.get('NFTGemPoolFactory')).address,
    sender
  );

  const marketHash = keccak256(['bytes'], [pack(['string'], ['PERL'])]);
  const poolAddress = await NFTGemPoolFactory.getNFTGemPool(marketHash);
  const pool = await new Contract(poolAddress, gemPoolData, sender);
  const tokensLen = await pool.allTokenHashesLength();

  console.log(`All token hashes length: ${tokensLen.toString()}`);
  for (let i = 0; i < 10; i++) {
    const tokenHash = await pool.allTokenHashes(i);
    const tokenId = await pool.tokenId(i);
    const tokenType = await pool.tokenType(i);
    console.log(`${tokenType.toString()} ${tokenId.toString()} ${tokenHash}`);
  }
}

// We recommend this pattern to be able to use async/await everywhere
// and properly handle errors.
main()
  .then(() => process.exit(0))
  .catch((error) => {
    console.error(error);
    process.exit(1);
  });

