let sender: any, senderAddress: any, deployedContracts: any;

describe('Wrap', async () => {
  before(async () => {
    [sender] = await ethers.getSigners();
    senderAddress = sender.address;
  });

  it('find the RUBY pool and wrapped token addresses', async () => {
    deployedContracts = await func(hre);

    const wrappedTokenAddress = await deployedContracts.ERC20GemTokenFactory.getItem(
      keccak256(['bytes'], [pack(['string'], ['WRUBY'])])
    );
    const erc20 = await getTinyERC20(sender, wrappedTokenAddress);
    const wrappedToken: any = await new Contract(
      wrappedTokenAddress,
      require('../build/ERC20WrappedGem.json'),
      sender
    );

    await deployedContracts.NFTGemMultiToken.setApprovalForAll(
      wrappedTokenAddress,
      true
    );
    await wrappedToken.wrap(1);
    const myBalance = await erc20.balanceOf(senderAddress);
    console.log(myBalance.toString());

    console.log(myBalance.toString());

    await erc20.approve(wrappedTokenAddress, myBalance);
    await wrappedToken.unwrap(1);
    const myNewBalance = await erc20.balanceOf(senderAddress);

    console.log(myNewBalance.toString());
  });
});

